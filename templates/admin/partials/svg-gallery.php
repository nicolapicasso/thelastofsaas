    let currentCallback = null;

    window.svgGallery = {
        open: function(callback) {
            currentCallback = callback;
            this.render('all');
            document.getElementById('svgGalleryModal').style.display = 'flex';
            document.getElementById('svgGallerySearch').value = '';
            document.getElementById('svgGallerySearch').focus();
        },

        close: function() {
            document.getElementById('svgGalleryModal').style.display = 'none';
            currentCallback = null;
        },

        render: function(category, search = '') {
            const grid = document.getElementById('svgGalleryGrid');
            grid.innerHTML = '';

            let icons = [];
            if (category === 'all') {
                Object.values(svgLibrary).forEach(cat => icons = icons.concat(cat));
            } else if (svgLibrary[category]) {
                icons = svgLibrary[category];
            }

            // Filter by search
            if (search) {
                const searchLower = search.toLowerCase();
                icons = icons.filter(icon =>
                    icon.name.toLowerCase().includes(searchLower)
                );
            }

            icons.forEach(icon => {
                const item = document.createElement('div');
                item.className = 'svg-gallery-item';
                item.innerHTML = icon.svg + `<span>${icon.name}</span>`;
                item.addEventListener('click', () => {
                    if (currentCallback) {
                        currentCallback(icon.svg);
                    }
                    svgGallery.close();
                });
                grid.appendChild(item);
            });

            if (icons.length === 0) {
                grid.innerHTML = '<div style="grid-column: 1/-1; text-align:center; padding: 40px; color: #999;">No se encontraron iconos</div>';
            }
        }
    };

    // Initialize event listeners
    document.addEventListener('DOMContentLoaded', function() {
        // Close button
        document.querySelector('.svg-gallery-close')?.addEventListener('click', () => svgGallery.close());
        document.querySelector('.svg-gallery-overlay')?.addEventListener('click', () => svgGallery.close());

        // Category buttons
        document.querySelectorAll('.svg-category').forEach(btn => {
            btn.addEventListener('click', function() {
                document.querySelectorAll('.svg-category').forEach(b => b.classList.remove('active'));
                this.classList.add('active');
                svgGallery.render(this.dataset.category, document.getElementById('svgGallerySearch').value);
            });
        });

        // Search input
        document.getElementById('svgGallerySearch')?.addEventListener('input', function() {
            const activeCategory = document.querySelector('.svg-category.active')?.dataset.category || 'all';
            svgGallery.render(activeCategory, this.value);
        });

        // ESC to close
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape' && document.getElementById('svgGalleryModal').style.display !== 'none') {
                svgGallery.close();
            }
        });
    });
